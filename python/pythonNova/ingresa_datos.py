'''
Realiza un programa que al introducir tu nombre, 
un numero entero y un numero decimal, los imprima 
como datos de salida
'''

nombre = input("Ingresa tu nombre: ")
numero_entero = int(input("Ingresa un numero entero: "))
numero_decimal = float(input("Ingresa un numero decimal: "))

print(f"El nombre ingresado es: {nombre}")
print(f"El numero entero ingresado es: {numero_entero}")
print(f"El numero decimal ingresado es: {numero_decimal}")
